
public class While20Ate10 {

	public static void main(String[] args) {

		// Criamos uma variável chamada "numero" e damos o valor inicial 20
		int numero = 20;

		// O "while" também é uma estrutura de repetição (loop).
		// Ele repete o código dentro das chaves ENQUANTO a condição for verdadeira.
		while (numero >= 10) {

			// Enquanto "numero" for maior ou igual a 10,
			// este comando imprime o valor atual no console.
			System.out.println(numero);

			// Aqui diminuímos 1 do valor de "numero" a cada repetição.
			numero--;
		}

	}

}
