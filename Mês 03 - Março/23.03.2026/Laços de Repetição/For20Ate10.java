
public class For20Ate10 {

	public static void main(String[] args) {

		for (int i = 20; i >= 10; i--) {
			// Parte 1: int i = 20
			// Criamos uma variável chamada "i" e começamos com o valor 20.

			// Parte 2: i >= 10
			// Essa é a condição para o loop continuar.
			// Enquanto "i" for maior ou igual a 10, o bloco será executado.

			// Parte 3: i--
			// A cada repetição, diminuímos 1 do valor de "i".

			System.out.println(i);
		}

	}

}
